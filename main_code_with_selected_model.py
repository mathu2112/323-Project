# ==========================
# Markdown 1
# ==========================
# # **Importing Required Libraries & Loading Dataset**


import pandas as pd
import numpy as np

df = pd.read_csv("/content/MiddleEast_dataset.csv")

# ==========================
# Markdown 2
# ==========================
# # **Dataset Overview**
# 
# Before preprocessing, it is important to understand the structure of the dataset.
# 
# This section provides information about:
# - Number of records
# - Number of features
# - Data types
# - Missing values
# 
# Understanding the dataset helps identify potential data quality issues that need to be addressed before model development.
# yeah

# Display dataset structure and identify missing values
df.info()

df.describe()

# ==========================
# Markdown 3
# ==========================
# # **DATA CLEANING AND PREPROCESSING**
# 
# This section performs:
# - Duplicate value analysis
# - Missing value analysis
# - Missing value treatment
# 
# Ensuring data quality improves model reliability and prediction performance.

# Check for duplicate project records
print("Number of duplicate rows:", df.duplicated().sum())

# Check number of missing values in each column
df.isnull().sum()

# Remove missing Emirate_Region
df = df.dropna(subset=["Emirate"])

# Replace missing values with the median
df["Budget"] = df["Budget"].fillna(
    df["Budget"].median()
)

df["Labor_Hours"] = df["Labor_Hours"].fillna(
    df["Labor_Hours"].median()
)

# ==========================
# Markdown 4
# ==========================
# **Data Conversion and Feature Engineering**
# 
# Project dates are converted into datetime format to enable time-based calculations and for feature engineering.
# 
# Converting date columns allows extraction of useful scheduling information that may influence project delays.

#Check data taypes
df.dtypes

# Convert date columns into datetime format for time calculations
df["Start_Date"] = pd.to_datetime(df["Start_Date"],dayfirst=True)
df["End_Date"] = pd.to_datetime(df["End_Date"],dayfirst=True)
df["Actual_End_Date"] = pd.to_datetime(df["Actual_End_Date"],dayfirst=True)

# ==========================
# Markdown 5
# ==========================
# **Target Variable Creation**
# 
# A binary target variable named On_Time_Status is created to classify project outcomes.
# 
# Classification Rules:
# - 0 = Project completed on time
# - 1 = Project delayed
# 
# The target variable is generated by comparing the Actual_End_Date with the planned End_Date.
# 
# This variable serves as the prediction target for the machine learning model.


# Create binary target variable:
# 0 = On Time
# 1 = Delayed
df['Timeline_Performance'] = np.where(
    df['Actual_End_Date'] > df['End_Date'],
    1,
    0
)

print(df['Timeline_Performance'].value_counts())

# ==========================
# Markdown 6
# ==========================
# **Feature Engineering**
# 
# Additional features are created from project dates to improve predictive performance.
# 
# Generated Features:
# - Planned_Duration
# - Start_Month
# - Start_DayOfWeek
# 
# These features provide scheduling information that may help identify patterns associated with project delays.

# Calculate planned project duration in days
df['Planned_Duration'] = (df['End_Date'] - df['Start_Date']).dt.days

# Extract month from project start date
df['Start_Month'] = df['Start_Date'].dt.month

# Extract day of week from project start date
df['Start_DayOfWeek'] = df['Start_Date'].dt.dayofweek

# ==========================
# Markdown 7
# ==========================
# ## **Feature** **Selection**
# 
# Certain columns are removed because they are either identifiers, contain information unavailable at prediction time, or may introduce data leakage.
# 
# Examples include:
# - Project_ID
# - Project_Name
# - Actual_End_Date
# - Cost
# - Cost_Variance
# 
# Only relevant predictive features are retained for model training.

# Remove identifiers and columns that may cause data leakage
df = df.drop(columns=[
    'Project_ID',
    'Project_Name',
    'Actual_End_Date',
    'Task_Status',
    'Cost',
    'Cost_Variance',
    'Start_Date',
    'End_Date'
])

print(df.head())

# ==========================
# Markdown 8
# ==========================
# ## **Encoding Categorical Variables**
# 
# Machine learning algorithms require numerical input.
# 
# Categorical variables such as:
# - Emirate
# - Type_of_Work
# - Contractor
# 
# are converted into numerical representations using Label Encoding.
# 
# This allows the Random Forest model to process categorical information effectively.

# Convert categorical variables into numerical values
from sklearn.preprocessing import LabelEncoder

le_emirate = LabelEncoder()
le_work = LabelEncoder()
le_contractor = LabelEncoder()

df['Emirate'] = le_emirate.fit_transform(df['Emirate'])
df['Type_of_Work'] = le_work.fit_transform(df['Type_of_Work'])
df['Contractor'] = le_contractor.fit_transform(df['Contractor'])

# ==========================
# Markdown 9
# ==========================
# ## **Defining Features and Target Variables**
# 
# The dataset is separated into:
# 
# - Feature matrix (X)
# - Target variable (y)
# 
# The feature matrix contains all predictor variables, while the target variable contains the project delay classification.

# Separate predictor variables and target variable
X = df.drop('Timeline_Performance', axis=1)

y = df['Timeline_Performance']

# ==========================
# Markdown 10
# ==========================
# ## **Train-Test Split**
# 
# The dataset is divided into training and testing sets.
# 
# - Training Set: Used to train the model.
# - Testing Set: Used to evaluate performance on unseen data.
# 
# An 80/20 split is used with stratified sampling to maintain the original class distribution.

from sklearn.model_selection import train_test_split

# Split dataset into training and testing sets
X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.20,
    random_state=42,
    stratify=y
)

print(df['Timeline_Performance'].value_counts())
print("Training Set:", X_train.shape)
print("Testing Set:", X_test.shape)

# ==========================
# Markdown 11
# ==========================
# **Base Random Forest Model Development**
# 
# A Random Forest Classifier is used to predict project delays.
# 
# Random Forest is an ensemble learning algorithm that combines multiple decision trees to improve accuracy and reduce overfitting.
# 
# The model is trained using historical ERP project data to learn patterns associated with project delays.

# Train Random Forest classifier
from sklearn.ensemble import RandomForestClassifier

rf = RandomForestClassifier(
    n_estimators=200,
    random_state=42,
    class_weight='balanced'
)

rf.fit(X_train, y_train)


# Make predictions on unseen test data
y_pred = rf.predict(X_test)

# Predict probabilities
probs = rf.predict_proba(X_test)

# Extract the probability of the "Delayed" and "On-time" class
ontime_prob = probs[:,0]
delay_prob = probs[:,1]

# Display prediction results for the first 6 projects in the test set
for i in range(6):

    # Convert numeric prediction to a readable label
    status = "Delayed" if y_pred[i] == 1 else "On-Time"

    # Assign risk level based on delay probability
    if delay_prob[i] < 0.30:
        risk = "Low"

    elif delay_prob[i] < 0.70:
        risk = "Medium"

    else:
        risk = "High"

    # Print prediction results
    print("Project", i+1)
    print("Prediction:", status)
    print("On-Time Probability:", round(ontime_prob[i]*100,2), "%")
    print("Delay Probability:", round(delay_prob[i]*100,2), "%")
    print("Risk Level:", risk)
    print()

# ==========================
# Markdown 12
# ==========================
# # **Model Evaluation** - (Accuracy, Classification Report)
# 
# 
# Initial Model Evaluation
# 
# The trained model is evaluated using classification metrics:
# 
# - Accuracy
# - Precision
# - Recall
# - F1-Score
# 
# These metrics provide insight into how effectively the model distinguishes between delayed and on-time projects.

#Evaluate the model
from sklearn.metrics import accuracy_score
from sklearn.metrics import classification_report
from sklearn.metrics import confusion_matrix

print("Accuracy:", accuracy_score(y_test, y_pred))

print("\nClassification Report:")
print(classification_report(y_test, y_pred))

print("\nConfusion Matrix:")
print(confusion_matrix(y_test, y_pred))

# ==========================
# Markdown 13
# ==========================
