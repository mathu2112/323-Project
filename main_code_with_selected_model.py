# ==========================
# Markdown 15
# ==========================
# # **Cross Validation**
# 
# Cross validation is performed to assess the stability and generalization ability of the model.
# 
# The dataset is divided into multiple folds, and the model is trained and evaluated repeatedly.
# 
# This provides a more reliable estimate of model performance compared to a single train-test split.

#Cross validation
from sklearn.model_selection import cross_val_score

scores = cross_val_score(
    rf,
    X,
    y,
    cv=5,
    scoring='accuracy'
)

print("Cross Validation Scores:")
print(scores)

print("\nAverage Accuracy:")
print(scores.mean())

# ==========================
# Markdown 16
# ==========================
# # **Hyperparameter Tuning**
# 
# Grid Search Cross Validation is used to identify the optimal Random Forest parameters.
# 
# Parameters tuned include:
# - Number of trees
# - Maximum tree depth
# - Minimum samples split
# - Minimum samples leaf
# 
# The goal is to maximize model performance while minimizing overfitting.

#Hyperparameter tuning
from sklearn.model_selection import GridSearchCV
from sklearn.ensemble import RandomForestClassifier

param_grid = {
    'n_estimators': [100, 200, 300],
    'max_depth': [5, 10, 15],
    'min_samples_split': [2, 5, 10],
    'min_samples_leaf': [1, 2, 4]
}

grid = GridSearchCV(
    estimator=RandomForestClassifier(random_state=42),
    param_grid=param_grid,
    cv=5,
    scoring='accuracy',
    n_jobs=-1
)

grid.fit(X_train, y_train)

print("Best Parameters:")
print(grid.best_params_)

print("\nBest Cross Validation Score:")
print(grid.best_score_)

best_rf = grid.best_estimator_

y_pred = best_rf.predict(X_test)

# ==========================
# Markdown 17
# ==========================
# # **Confusion Matrix**
