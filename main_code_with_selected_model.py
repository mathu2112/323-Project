# ### **Predicted Probability Distribution**
# 
# While classification metrics such as Accuracy, Precision, Recall, and F1-Score tell us how well the model performs overall, they do not show how confident the model is when making predictions.
# 
# Random Forest provides a probability score for each class. Instead of simply predicting whether a project is "On Time" or "Delayed", the model estimates the probability that a project will be delayed.
# 
# This visualization compares the predicted delay probabilities for:
# 
# - Actual On Time projects
# - Actual Delayed projects
# 
# A well-performing model should assign:
# 
# - Low delay probabilities to projects that are actually On Time.
# - High delay probabilities to projects that are actually Delayed.
# 
# The greater the separation between the two distributions, the better the model is at distinguishing between the two classes.

# Obtain the predicted probability of the positive class (Delayed = 1)
# predict_proba() returns probabilities for both classes:
# Column 0 = Probability of On Time
# Column 1 = Probability of Delayed
y_prob = best_rf.predict_proba(X_test)[:, 1]

# Plot the probability distribution separately for actual On Time vs actual Delayed cases
plt.hist(y_prob[y_test==0], bins=20, alpha=0.5, label='Actual: On Time')
plt.hist(y_prob[y_test==1], bins=20, alpha=0.5, label='Actual: Delayed')
plt.xlabel('Predicted Probability of Delay')
plt.ylabel('Number of Projects')
plt.title('Predicted Probability Distribution')
plt.legend()
plt.show()
